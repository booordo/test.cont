    <script>
        (function () {

            function SideBar(options) {
                this.id = options.id;
                this.element = document.getElementById(this.id);
                this.parent = this.element.parentNode;
                this.classNameActiveElement = "price-category_actived";
                this.classNameActiveElementToBottom = "price-category_actived-bottom";
                this.classNameLink = "price-category__link";
                this.classNameActiveLink = "price-category__link_actived";
                this.links = Array.prototype.slice.call(this.element.getElementsByClassName(this.classNameLink));
                this.targets = [];
                this.height = this.element.offsetHeight;
                this.padding = options.padding;
                this.timer = null;
                this.target = null;
                this.boundTop = this.element.offsetTop;
                this.boundBottom = this.parent.offsetTop + this.parent.offsetHeight - this.element.offsetHeight;
                this.speed = 0;
                this.minSpeed = options.minSpeed;
                this.direction = "";
                this.interval = 1000 / 60;
                this.linkActivedByClick = false;
                this.setHandlers();
                this.setTargets();
            }
        
            SideBar.prototype.setHandlers = function () {
                var self = this;
                this.links.forEach(function (element) {
                    element.addEventListener("click", self.clickHandler.bind(self));
                });
                document.addEventListener("scroll", this.scrollHandler.bind(this));
            };
            
            SideBar.prototype.setTargets = function () {
                var self = this;
                this.links.forEach(function (element) {
                    var target = {};
                    target.link = element;
                    target.element = document.getElementById(element.href.replace(/[^#]*#(\w*)/, '$1'));
                    target.start = target.element.offsetTop - self.padding;
                    target.end = target.start + target.element.offsetHeight + 50;
                    self.targets.push(target);
                });
            };
        
            SideBar.prototype.clickHandler = function (event) {
                clearTimeout(this.timer);
                this.setTarget(event);
                this.setActiveLink(event);
                this.setDirection();
                this.setSpeed();
                this.scrollToTarget();
                event.preventDefault();
            };
        
            SideBar.prototype.setTarget = function (event) {
                this.target = document.getElementById(event.target.href.replace(/[^#]*#(\w*)/, '$1'));
            };
        
            SideBar.prototype.setActiveLink = function (event) {
                var self = this;
                this.links.forEach(function (element) {
                    element.classList.remove(self.classNameActiveLink);
                });
                event.target.classList.add(this.classNameActiveLink);
            };
        
            SideBar.prototype.setDirection = function () {
                this.direction = this.getRangeToTarget() > 0 ? "down" : "top";
            };
        
            SideBar.prototype.setSpeed = function () {
                var speed = this.getRangeToTarget() / this.interval;
                if (this.direction == "top") {
                    this.speed = speed < this.minSpeed ? speed : this.minSpeed;
                } else {
                    this.speed = speed > this.minSpeed ? speed : this.minSpeed;
                }
            };
        
            SideBar.prototype.scrollToTarget = function () {
                var self = this;
                this.linkActivedByClick = true;
                this.timer = setTimeout(function scroll() {
                    window.scrollBy(0, self.getStep());
                    if (self.scrollEnd()) {
                        self.linkActivedByClick = false;
                        return;
                    }
                    self.timer = setTimeout(scroll, self.interval);
                }, this.interval);
            };
        
            SideBar.prototype.getStep = function () {
                var range = this.getRangeToTarget() - this.padding;
                if (this.direction == "top") return range > this.speed ? range : this.speed;
                if (this.direction == "down") return range < this.speed ? range : this.speed;
            };
        
            SideBar.prototype.scrollEnd = function () {
                return this.getRangeToTarget() == this.padding;
            };
        
            SideBar.prototype.scrollHandler = function (event) {
                this.setPosition();
                this.activateLink();
            };
        
            SideBar.prototype.setPosition = function () {
                if (this.isFixed()) {
                    if (!this.boundTopIsCrossed() || this.boundBottomIsCrossed()) {
                        this.element.classList.remove(this.classNameActiveElement);
                        if (this.boundBottomIsCrossed()) {
                            this.parent.style.position = "relative";
                            this.element.classList.add(this.classNameActiveElementToBottom);
                        }
                    }
                } else {
                    if (this.boundTopIsCrossed() && !this.boundBottomIsCrossed()) {
                        this.parent.style.position = "static";
                        this.element.classList.add(this.classNameActiveElement);
                        this.element.classList.remove(this.classNameActiveElementToBottom);
                    }
                }
            };
            
            SideBar.prototype.activateLink = function () {
                if (!this.linkActivedByClick) {
                    var self = this;
                    this.links.forEach(function (element) {
                        element.classList.remove(self.classNameActiveLink);
                    });
                    this.targets.forEach(function (target) {
                        if (target.start <= window.pageYOffset && target.end > window.pageYOffset) {
                            target.link.classList.add(self.classNameActiveLink);
                        }
                    });
                }
            };
        
            SideBar.prototype.isFixed = function () {
                return this.element.classList.contains(this.classNameActiveElement);
            };
        
            SideBar.prototype.boundTopIsCrossed = function () {
                return this.getRangeToBoundTop() < this.padding;
            };
        
            SideBar.prototype.boundBottomIsCrossed = function () {
                return this.getRangeToBoundBottom() < 50;
            };
        
            SideBar.prototype.getRangeToTarget = function () {
                return this.target.offsetTop - window.pageYOffset;
            };
        
            SideBar.prototype.getRangeToBoundTop = function () {
                return this.boundTop - window.pageYOffset;
            };
        
            SideBar.prototype.getRangeToBoundBottom = function () {
                return this.boundBottom - window.pageYOffset;
            };
        
            window.addEventListener("load", function () {
                var sideBar = new SideBar({
                    id: "sidebar",
                    minSpeed: 50,
                    padding: 10
                });
            });
        
        })();
